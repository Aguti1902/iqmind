// Función helper para obtener traducciones
export function getEmailTranslation(lang: string, key: keyof typeof emailTranslations.es): string {
  // Si el idioma no está soportado, usar inglés por defecto
  const supportedLang = ['es', 'en', 'fr', 'de', 'it', 'pt', 'sv', 'no', 'uk'].includes(lang) ? lang : 'en'
  return emailTranslations[supportedLang as keyof typeof emailTranslations][key]
}

